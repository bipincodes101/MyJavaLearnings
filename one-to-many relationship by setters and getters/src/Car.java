
public class Car {
	//decelaration of instance variables.
	String carName;
	String carModel;
	Integer carPrice;
	String carColor;
	//setters and getters for the instance variables.
	//seters and getters for carName variable.
	String getCarName() {
		return carName;
	}
	void setCarName(String carName) {
		this.carName=carName;
	}
	//setters and getters for carModel.
	String getCarModel() {
		return carModel;
	}
	void setCarModel(String carModel) {
		this.carModel=carModel;
	}
	//setters and getters for carPrice.
	Integer getCarPrice() {
		return carPrice;
	}
	void setCarPrice(int carPrice) {
		this.carPrice=carPrice;
	}
	//setters and getters for carColor.
	String getCarColor() {
		return carColor;
	}
	void setCarColor(String carColor) {
		this.carColor=carColor;
	}
}
